/* Functions */
var name = 'Megan', age = 20

/* Without parameters */
function printName()
{
    console.log(`${name} has ${age} years old`)
}

/* Calling the function */
printName()


/* Using parameters */
function printData(nPerson, aPerson)
{
    console.log(`${nPerson} has ${aPerson} years old`)
}
/* Calling the function */
printData('Megan', 20)